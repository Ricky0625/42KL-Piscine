int	ft_strcmp(char *s1, char *s2)
{
	int	diff;

	diff = 0;
	while (*s1 == *s2)
	{
		if (*s1 == '\0' && *s2 == '\0')
			return (0);
		s1++;
		s2++;
	}
	diff = *s1 - *s2;
	if (diff < 0)
		return (-1);
	else
		return (1);
	return (diff);
}
